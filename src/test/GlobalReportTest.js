function update(data) {


  for (let i = 0; i < data.year.length; i++) {
    for (let j = 0; j < data.year[i].date.length; j++) {
      // console.log(data.year[i].date[j])

      let industryUsage = 0;
      for (let [key, value] of Object.entries(data.year[i].date[j].industry)) {
        industryUsage += data.year[i].date[j].industry[key];
      }
      data.year[i].date[j].industryUsage = industryUsage;

      let fuelUsage = 0;
      for (let [key, value] of Object.entries(data.year[i].date[j].city)) {
        fuelUsage += data.year[i].date[j].city[key];
      }
      data.year[i].date[j].fuelUsage = fuelUsage;

      data.year[i].date[j].salesTotal = data.year[i].date[j].electricityUsage +
        data.year[i].date[j].fuelUsage + data.year[i].date[j].industryUsage;
    }

    let sum=0;
    for (let t = 0; t < data.year[i].date.length; t++) {
      sum += data.year[i].date[t].total;
    }
    data.year[i].total = sum;
  }


  let sum = 0;
  for (let i = 0; i < data.year.length; i++) {
    sum += data.year[i].total
  }
  data.total = sum;
}

